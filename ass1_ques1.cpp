//Accept a number at run-time, calculate and display its square and cube.
#include<iostream>
using namespace std;
int main()
{
	int n1;
	cout<<"ENTER THE VALUE FOR n1: ";
	cin>>n1;
	int square=n1*n1;
	cout<<"THE NUMBER SQUARE IS\n"<<square<<endl;
	int cube=n1*n1*n1;
	cout<<"THE CUBE OF NUMBER IS"<<cube;
	return 0;
	
}
